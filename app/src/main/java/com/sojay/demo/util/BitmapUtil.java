    /**
     * 获取水印（logo + 房间号）
     * @param text 房间号
     * @param source logo
     * @param textSize 字体大小
     * @param padding  字体与logo间距
     * @return
     */
    public static Bitmap setTextToImg(String text, Bitmap source, int textSize, int padding){

        //获取文字宽高
        Paint textPaint = new Paint(Paint.ANTI_ALIAS_FLAG);
        textPaint.setColor(Color.parseColor("#80ffffff"));
        textPaint.setTextSize(textSize);
        Rect bounds = new Rect();
        textPaint.getTextBounds(text, 0, text.length(), bounds);

        //设置新的bitmap的最大宽高
        int bitmapWidth = bounds.width() > source.getWidth() ? bounds.width() : source.getWidth();
        int bitmapHeight = source.getHeight() + padding + bounds.height() ;

        //新创建一个新图片比源图片多出一部分，后续用来与文字叠加用
        Bitmap newBitmap = Bitmap.createBitmap(bitmapWidth, bitmapHeight + 10, Bitmap.Config.ARGB_8888);

        Canvas canvas = new Canvas(newBitmap);

        //把图片画上来
        Paint bitmapPaint = new Paint();
        canvas.drawBitmap(source,bitmapWidth - source.getWidth(),0, bitmapPaint);

        //画文字
        canvas.drawText(text, 0, bitmapHeight, textPaint);

        return newBitmap;
    }

